package junsuck;

public class Ex2_1 {
    public static void main(String[] args) {
        // 변수가 필요한 이유
        // 변수의 값만 바꿔도 여러 값을 구할 수 있음
        int x = 3, y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
